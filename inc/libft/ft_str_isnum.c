#include "libft.h"

bool	ft_str_isnum(const char *str)
{
	if (*str == '-')
		str++;
	while (*str)
	{
		if (ft_char_isdigit(*str) == false && *str != '.')
			return (false);
		str++;
	}
	return (true);
}
